import { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
    return {
        rules: [
            {
                userAgent: '*',
                allow: '/',
                disallow: ['/dashboard/', '/admin/', '/api/', '/auth/', '/onboarding/'],
            },
        ],
        sitemap: 'https://cevace.com/sitemap.xml',
    }
}
